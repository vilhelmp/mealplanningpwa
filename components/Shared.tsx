export const Button = ({ children, onClick, variant = 'primary', className = '', type = 'button', disabled = false }: any) => {
  const baseStyle = "px-4 py-3 rounded-xl font-semibold transition-all duration-200 flex items-center justify-center gap-2 active:scale-95 disabled:opacity-50 disabled:pointer-events-none";
  const variants = {
    primary: "bg-nordic-primary text-white shadow-lg shadow-teal-900/10 hover:bg-teal-700",
    secondary: "bg-white text-nordic-secondary border border-gray-200 hover:bg-gray-50",
    ghost: "bg-transparent text-nordic-muted hover:bg-gray-100",
    danger: "bg-red-50 text-red-600 hover:bg-red-100"
  };
  
  return (
    <button type={type} onClick={onClick} className={`${baseStyle} ${variants[variant as keyof typeof variants]} ${className}`} disabled={disabled}>
      {children}
    </button>
  );
};

